void RTCGetTimeTimer()
{
	i2cGetRTCValues(); 
	RTC_read_count = 0;
	adaptiveStartTimeCheck();
}

void counters()
{ // every 1 sec, this goes by 25 ticks
	static uint16_t temperature_count = 0;
	/**** counters *****/
	display_toggle_count > 0 ? increment_counter(&display_toggle_count) : (void)0;
	RTC_read_count == RTC_GET_TIME ? RTCGetTimeTimer() : increment_counter(&RTC_read_count);
	temperature_count % TEMPERATURE_GET 		== 0 ? getTemperature() : increment_counter(&temperature_count);
	test_check_count 	% HARDWARE_TEST_CHECK != 0 ? increment_counter(&test_check_count) : hardwareTest();
	/*** expressions ***/
	screen_toggle_counter_expression(&display_toggle_count);
	heatingSetTargetTemperature(display_status.currentTarget*10); //Triac control
	openWindowTimer();
	LEDBlinkingcount();
}



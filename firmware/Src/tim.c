void increment_counter(uint16_t *counter)
{
	*counter += 1;
}

void screen_toggle_counter_expression(uint16_t *counter)
{
	if(*counter == START_DIMMING_SCREEN)
		{
			*counter = 1;
			startDimming = true;
			flashWriteHalfword();
		}
	/***************/
	if (startDimming)
	{
		if(*counter % DIMMING_STEP == 0)
		{
			if (!displayDecrementScreenRate())
			{
				startDimming = false;
				*counter = 0;
			}
		}
	}
}

